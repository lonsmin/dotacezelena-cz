<template>
    <div class="cont">
        <div class="slider-component">
            <div class="slidecontainer">
                <input
                    ref="input"
                    v-model="currentValue"
                    type="range"
                    :step="step"
                    :min="min"
                    :max="max"
                    class="slider"
                    @input="onInput"
                />
            </div>
        </div>
    </div>
</template>

<script>
export default {
    props: {
        step: {
            type: Number,
            required: true,
        },
        value: {
            type: Number,
            required: true,
        },
        min: {
            type: Number,
            required: true,
        },
        max: {
            type: Number,
            required: true,
        },
    },
    data() {
        return {
            currentValue: this.value,
        };
    },
    methods: {
        onInput() {
            this.$emit("input", parseInt(this.currentValue));
        },
    },
};
</script>

